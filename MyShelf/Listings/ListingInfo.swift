import Foundation

class ListingInfo{
    var sellerName: String
    var condition: String
    var price: String
    var bookName: String
    
    init(sellerName: String, condition: String, price: String, bookName: String) {
        self.sellerName = sellerName
        self.condition = condition
        self.price = price
        self.bookName = bookName
    }
    
    
}
